//Main Function
int main()
{
	int m=40, n=20;

	if(m>n)
	{
		printf("\n m is greater than n");
	}

	else if(m<n)
	{
		printf("\n m is smaller than n");
	}

	else
	{
		printf("\n m is equal to n");
	}
	printf("\n");

	return 0;

}

